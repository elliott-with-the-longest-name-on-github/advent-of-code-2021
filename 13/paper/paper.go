package paper

import (
	"fmt"
	"strconv"
	"strings"
)

type Dot struct {
	X int
	Y int
}

type Paper struct {
	Dots map[string]*Dot
	MaxX int
	MaxY int
}

func PartOne() string {
	paper := ChallengePaper
	instruction := FoldInstructions[0]
	newPaper, err := paper.Fold(instruction.Direction, instruction.Line)
	if err != nil {
		panic(err)
	}
	return fmt.Sprintf("Part One: %v dots visible after the first fold.", newPaper.VisibleDots())
}

func PartTwo() string {
	paper := &ChallengePaper
	for _, ins := range FoldInstructions {
		var err error
		paper, err = paper.Fold(ins.Direction, ins.Line)
		if err != nil {
			panic(err)
		}
	}
	return fmt.Sprint("Part Two: \n" + paper.String())
}

func New(dots map[string]*Dot, MaxX int, MaxY int) Paper {
	// This is a little lazy -- we should make sure MaxX and MaxY aren't smaller than any of the
	// dots we were given. But I don't want to right now.
	paper := Paper{
		Dots: dots,
		MaxX: MaxX,
		MaxY: MaxY,
	}

	if MaxX == 0 && MaxY == 0 {
		paper.MaxX = paper.maxX().X
		paper.MaxY = paper.maxY().Y
	}

	return paper
}

func (p *Paper) Fold(direction string, line int) (*Paper, error) {
	var newPaper *Paper
	var err error
	if direction == "up" {
		newPaper, err = p.foldUp(line)
		if err != nil {
			return nil, err
		}
	} else if direction == "left" {
		newPaper, err = p.foldLeft(line)
		if err != nil {
			return nil, err
		}
	} else {
		return nil, fmt.Errorf("invalid fold direction: %v, need \"up\" or \"left\"", direction)
	}
	return newPaper, nil
}

func (p *Paper) VisibleDots() int {
	return len(p.Dots)
}

func (p *Paper) String() string {
	var sb strings.Builder
	maxX := p.MaxX
	maxY := p.MaxY
	for y := 0; y <= maxY; y++ {
		for x := 0; x <= maxX; x++ {
			key := strconv.Itoa(x) + "," + strconv.Itoa(y)
			if _, exists := p.Dots[key]; exists {
				sb.WriteString("#")
			} else {
				sb.WriteString(".")
			}
		}
		sb.WriteString("\n")
	}
	return sb.String()
}

func (p *Paper) foldUp(line int) (*Paper, error) {
	newDots := map[string]*Dot{}
	for k, point := range p.Dots {
		// lines will never have dots
		if point.Y > line {
			newY := line - (point.Y - line)
			newKey := strconv.Itoa(point.X) + "," + strconv.Itoa(newY)
			newDots[newKey] = &Dot{X: point.X, Y: newY}
		} else {
			newDots[k] = point
		}
	}
	newPaper := &Paper{
		Dots: newDots,
		MaxX: p.MaxX,
		MaxY: line - 1,
	}
	return newPaper, nil
}

func (p *Paper) foldLeft(line int) (*Paper, error) {
	newDots := map[string]*Dot{}
	for k, point := range p.Dots {
		// lines will never have dots
		if point.X > line {
			newX := line - (point.X - line)
			newKey := strconv.Itoa(newX) + "," + strconv.Itoa(point.Y)
			newDots[newKey] = &Dot{X: newX, Y: point.Y}
		} else {
			newDots[k] = point
		}
	}
	newPaper := &Paper{
		Dots: newDots,
		MaxX: line - 1,
		MaxY: p.MaxY,
	}
	return newPaper, nil
}

func (p *Paper) maxX() *Dot {
	max := 0
	keyOfMax := ""
	for k, point := range p.Dots {
		if point.X > max {
			max = point.X
			keyOfMax = k
		}
	}
	return p.Dots[keyOfMax]
}

func (p *Paper) maxY() *Dot {
	max := 0
	keyOfMax := ""
	for k, point := range p.Dots {
		if point.Y > max {
			max = point.Y
			keyOfMax = k
		}
	}
	return p.Dots[keyOfMax]
}

