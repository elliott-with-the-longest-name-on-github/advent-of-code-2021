package submarine

import (
	"fmt"
)

type Instruction struct {
	Direction string
	Value     int
}

// The submarine can only go up/down/forward, so it's still only a Cartesian coordinate system.
type Submarine struct {
	Distance int
	Depth    int
	Aim      int
}

func PartTwo() string {
	sub := Submarine{}
	err := sub.Move(ChallengeInstructions...)
	if err != nil {
		panic(err)
	}
	return fmt.Sprintf("Submarine moved %v units.", sub.FinalDistance())

}

func (s *Submarine) Move(instructions ...Instruction) error {
	for _, instruction := range instructions {
		switch instruction.Direction {
		case "forward":
			s.Distance += instruction.Value
			s.Depth += s.Aim * instruction.Value
			if s.Depth < 0 {
				return fmt.Errorf("submarines can't fly! Instruction to move up of value %v would take submarine from %v depth to %v depth", instruction.Value, s.Depth+instruction.Value, s.Depth)
			}
		case "down":
			s.Aim += instruction.Value
		case "up":
			s.Aim -= instruction.Value
		}
	}
	return nil
}

func (s *Submarine) FinalDistance() int {
	return s.Distance * s.Depth
}

